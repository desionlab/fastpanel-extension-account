import Mongoose from 'mongoose';

/**
 * 
 */
export interface IOrganization extends Mongoose.Document {
  
  /**
   * Status of the enabled record.
   */
  enabled?: boolean;

  /* ----------------------------------------------------------------------- */

  /**
   * 
   */
  createdAt?: Date;

  /**
   * 
   */
  updatedAt?: Date;

  /**
   * Current version of the record.
   */
  version?: number;
};

/**
 * 
 */
export const OrganizationSchema = new Mongoose.Schema({
  
  /**
   * Status of the enabled record.
   */
  enabled: {
    type: Mongoose.Schema.Types.Boolean,
    default: true
  }
}, {
  /* Set (collection) table name. */
  collection: 'accountOrganization',
  /* Logger date. */
  timestamps: { 
    createdAt: 'createdAt',
    updatedAt: 'updatedAt'
  },
  /* Current version of the record. */
  versionKey: 'version',
  /* Converts the mongoose document into a plain javascript object. */
  toObject: {
    getters: true,
    virtuals: true
  },
  toJSON: {
    getters: true,
    virtuals: true
  }
});

OrganizationSchema.plugin(require('mongoose-autopopulate'));
OrganizationSchema.plugin(require('mongoose-hidden')(), {
  hidden: {
    version: false
  }
});

export const OrganizationModel = Mongoose.model<IOrganization>('Account.Organization', OrganizationSchema);

